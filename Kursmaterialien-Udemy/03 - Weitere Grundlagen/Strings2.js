"use strict"

// Newline
console.log('Hallo\\\nWelt!')

// String wiederholen
console.log("-".repeat(20))

// .slice(start, end); erste Zahl wo angefangen wird, zweite Zahl bis wohin ist
console.log("Hallo Welt".slice(6, 8))

// .substr(start, length); erst Zahl wo angefangen wird, zweite Zahl wie lang die Kette ist
console.log("Hallo Welt".substr(6, 2))
